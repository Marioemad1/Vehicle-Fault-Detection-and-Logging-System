void TEMP_getvalue(u16* temp,u8 channale_number )
{
		u32 result_scaled;
		ADC_getdigitalvalue(channale_number,temp);
		result_scaled = ((u32)(*temp) * 500UL) / 1023UL;
		*temp = (u16)(result_scaled / 100UL)*10;
}

void TEMP_init(void)
{
	DIO_SetPinDirection(TEMP_PORT,TEMP_PIN,DIO_PIN_OUTPUT);
	DIO_SetPin(TEMP_PORT,TEMP_PIN,DIO_PIN_HIGH);
}

void TEMP_kill(void)
{
	DIO_SetPin(TEMP_PORT,TEMP_PIN,DIO_PIN_LOW);
}
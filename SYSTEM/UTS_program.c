/*APIS*/
void UTS_getdistance(u16* distance,u8 channale_number )
{
	ADC_getdigitalvalue(channale_number,distance);
	*distance = (u16)(((u32)(*distance) * 100UL) / 1023UL);
}

void UTS_init()
{
		DIO_SetPinDirection(UTS_PORT,UTS_PIN,DIO_PIN_OUTPUT);
		DIO_SetPin(UTS_PORT,UTS_PIN,DIO_PIN_HIGH);
}

void UTS_kill()
{
	DIO_SetPin(UTS_PORT,UTS_PIN,DIO_PIN_LOW);
}